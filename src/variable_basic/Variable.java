package variable_basic;

public class Variable {
	
	
	 public static void main(String[] args) 
	{
	
		 
		/* multiple line comments
		 * 1. Declarations of variables
		 * 2. Assignment / Initialization of variable 
		 * 3. variable usage
		 */
		
		//variable declaration
		 
		Float Marks; 
		String name;   
		
		//variable initialization
		
		Marks=60.58f;   
		name="pankaj";  
			
		//variable usage
		
		System.out.println("My post-graduation percentage is "+Marks); //variable usage
		System.out.println("My name is "+name);  //variable usage  
		System.out.println("Velocity corporate training centre");  //variable usage
			
	 }
	 
	

}
