package controlstatement.Looping_statement;

public class While_loop 
{

	public static void main(String[] args) 
	{
		System.out.println("While loop increment and decrement");
		
		System.out.println("-----------------------------------");
		
		char i = 'A';   //declaration & initialization
		
		while(i<='J') //condition
		{
			System.out.print(i+" ");
		
		    i++;   //increment & decrement operation
		    
        }
		
		System.out.println("-----------------------------------");
		
		char a = 'J';
		
		while(a>='A')
		{
			System.out.print(a+" ");
			
			a--;
		}
		
		
		
		
	}

}
