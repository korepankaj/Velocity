package controlstatement.Conditional_statement;

public class If_else {

	public static void main(String[] args) 
	{
		// //if my grade is A i got above 90% marks else i got below 90%
		
		char grade = 'A';
		
		if (grade == 'A')
		{
			System.out.println("I got above 90% marks"); //Print statement
		}
		else
		{
			System.out.println("I got below 90% marks"); //print statement
		}

	}

}
